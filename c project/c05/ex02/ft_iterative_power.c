int	ft_iterative_power(int nb, int power)
{
	int	nbr;

	nbr = nb;
	while (power > 1)
	{
		nbr *= nb;
		power--;
	}
	if (power == 0)
	{
		return (1);
	}
	if (power < 0)
	{
		return (0);
	}
	return (nbr);
}
